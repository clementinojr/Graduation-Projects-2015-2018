    private static ObjetoC correntistaOp(java.lang.String codigo) throws ClassNotFoundException_Exception, SQLException_Exception {
        wspacote.WSJoseM_Service service = new wspacote.WSJoseM_Service();
        wspacote.WSJoseM port = service.getWSJoseMPort();
        return port.correntistaOp(codigo);
    }

    private static Correntista returnObj(java.lang.String arg0) {
        cliente.sdayle.ImplService service = new cliente.sdayle.ImplService();
        cliente.sdayle.Servico port = service.getImplPort();
        return port.returnObj(arg0);
    }

    private static ObjectC correntista(java.lang.String codigo) {
        clientes.eduardo.WSEduardo service = new clientes.eduardo.WSEduardo();
        clientes.eduardo.WSEduardoSoap port = service.getWSEduardoSoap();
        return port.correntista(codigo);
    }

    private static Co correntista_1(java.lang.String codigo) {
        clientes.rafael.WSRafael_Service service = new clientes.rafael.WSRafael_Service();
        clientes.rafael.WSRafael port = service.getWSRafaelPort();
        return port.correntista(codigo);
    }

   

    
    
