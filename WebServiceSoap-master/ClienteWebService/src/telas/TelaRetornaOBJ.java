    private void buttonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConsultarActionPerformed
        // TODO add your handling code here:
        if(CBoxJOBJ.isSelected()){
               String codigoJunior="";
                codigoJunior=  campoCodOBJ.getText();
                
                ObjetoC resultado = null;
            try {
                resultado = correntistaOp(codigoJunior);
            } catch (ClassNotFoundException_Exception ex) {
                Logger.getLogger(TelaRetornaOBJ.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException_Exception ex) {
                Logger.getLogger(TelaRetornaOBJ.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(resultado==null){
                JOptionPane.showMessageDialog(null, "Usuário inexistente");
                
            }
            else{
                   try {
                       
                       JOptionPane.showMessageDialog(null,"Código:"+ correntistaOp(codigoJunior).getCodigo()+"  |     Nome: "+correntistaOp(codigoJunior).getNome()+" |     Valor: "+ correntistaOp(codigoJunior).getValor());
                       campoCodOBJ.setText("");
                   } catch (ClassNotFoundException_Exception ex) {
                       Logger.getLogger(TelaRetornaOBJ.class.getName()).log(Level.SEVERE, null, ex);
                   } catch (SQLException_Exception ex) {
                       Logger.getLogger(TelaRetornaOBJ.class.getName()).log(Level.SEVERE, null, ex);
                   }
                
            }
                
            
        }
        if(CBoxROBJ.isSelected()){
            String codigoEdu="";
            codigoEdu = campoCodOBJ.getText();
            correntista(codigoEdu);
            if( correntista(codigoEdu)==null){
                 JOptionPane.showMessageDialog(null, "Usuário inexistente");
                
            }
            else{
                JOptionPane.showMessageDialog(null,"Código:"+ correntista(codigoEdu).getCodigo()+"  |     Nome: "+correntista(codigoEdu).getNome()+" |     Valor: "+ correntista(codigoEdu).getValor());
                campoCodOBJ.setText("");
                
            }
            
            
            
            
            
        }
        if(CBoxSOBJ.isSelected()){
            String codigoSdayle="";
                codigoSdayle=  campoCodOBJ.getText();
                Correntista resultadoSdayle =null;
                resultadoSdayle = returnObj(codigoSdayle);
                if(resultadoSdayle==null){
                JOptionPane.showMessageDialog(null, "Usuário inexistente");
                
            }
                else{
                    JOptionPane.showMessageDialog(null,"Código:"+ returnObj(codigoSdayle).getCodigo()+"  |     Nome: "+returnObj(codigoSdayle).getNome()+" |     Valor: "+ returnObj(codigoSdayle).getValor());
                    campoCodOBJ.setText("");
                
            }}
                
                if(campoSelecaoRafael.isSelected()){
                    
                    String codigoRafael ="";
                    Co resultadoRafael =null;
                     codigoRafael = campoCodOBJ.getText();
                    resultadoRafael= correntista_1(codigoRafael);
                
                    if(correntista_1(codigoRafael)==null){
                        JOptionPane.showMessageDialog(null, "Usuário inexistente");
                        
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Código:"+ correntista_1(codigoRafael).getCodigo()+"  |     Nome: "+correntista_1(codigoRafael).getNome()+" |     Valor: "+ correntista_1(codigoRafael).getValor());
                    campoCodOBJ.setText("");
                        
                    }
                
                
    
            
            
        }
    }//GEN-LAST:event_buttonConsultarActionPerformed

